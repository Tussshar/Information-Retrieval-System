    private void populateComboBox(ArrayList<Integer> foundDocuments, ArrayList<Document> documentList) {

        cmbFile.removeAllItems();
        
        for (Document doc : documentList) {
            if (foundDocuments.contains(doc.getDocumentId())) {
                cmbFile.addItem(doc);
            }
        }
    }

